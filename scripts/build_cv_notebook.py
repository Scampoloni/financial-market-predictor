     "for ticker in TEST_TICKERS:\n"
     "    mask = ticker_arr == ticker\n"
     "    axes[0].scatter(pca_result[mask, 0], pca_result[mask, 1],\n"
     "                    c=ticker_colors[ticker], label=ticker, alpha=0.7, s=60)\n\n"
     "axes[0].set_xlabel('PC1')\n"
     "axes[0].set_ylabel('PC2')\n"
     "axes[0].set_title('EfficientNet Embeddings - PCA Space (PC1 vs PC2)')\n"
     "axes[0].legend()\n\n"
     "axes[1].bar(range(1, 11), pca.explained_variance_ratio_ * 100, color='steelblue', alpha=0.8)\n"
